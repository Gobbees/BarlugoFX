package com.barlugofx.model.tools.common;

/**
 * This enum contains the possible parameters a filter can have. Note that each filter have a specific parameter that accepts.
 *
 */
public enum ParametersName {
    /**
     * parameter for CONTRAST filter.
     */
    CONTRAST,
    /**
     * parameter for SATURATION filter.
     */
    SATURATION,
    /**
     *  parameter for HUE filter.
     */
    HUE,
    /**
     * parameter for RGBCHANGES filter.
     */
    RED,
    /**
     * parameter for RGBCHANGES filter.
     */
    BLUE,
    /**
     * parameter for RGBCHANGES filter.
     */
    GREEN,
    /**
     * parameter for BlackAndWhite filter.
     */
    WBLUE,
    /**
     * parameter for BlackAndWhite filter.
     */
    WGREEN,
    /**
     * parameter for BlackAndWhite filter.
     */
    WRED,
    /**
     * parameter for EXPOSURE (BRIGHTNESS in HSB) filter.
     */
    EXPOSURE,
    /**
     * parameter for WHITEBALANCE filter.
     */
    WHITEBALANCE,
    /**
     * parameter for BRIGHTNESS filter.
     */
    BRIGHTNESS;
}
